namespace ManagedBass
{
    /// <summary>
    /// BitsPerSample of a Channel
    /// </summary>
    public enum Resolution
    {
        /// <summary>
        /// 16-Bit PCM (Default)
        /// </summary>
        Short,

        /// <summary>
        /// 8-Bit PCM
        /// </summary>
        Byte,

        /// <summary>
        /// 32-Bit IEEE Floating Point
        /// </summary>
        Float
    }
}